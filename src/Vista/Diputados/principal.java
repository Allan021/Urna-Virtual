    private void Ganadores2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_Ganadores2ItemStateChanged
        if(evt.getStateChange() == ItemEvent.SELECTED){
           
            if(Ganadores2.getSelectedIndex() !=0){
            Color blanco = new Color(200, 205, 208);
            
            diputado = (Diputado) Ganadores2.getSelectedItem();
              int  limite =  modelo.LimiteDiputados(diputado.getId_Departamento());
            
              PartidoPolitico partido1 = new PartidoPolitico();
          
              partido1 = partido1.partido(diputado.getId_Partido());//obteniendo el partido dependiendo con el id de partido del diputado
            
            
            campoNombre.setText(diputado.getNombre());
            campoNombre.setForeground(blanco);
            campoNombre.setHorizontalAlignment(0);
            
            
            campoVotos.setText(String.valueOf(diputado.getVotos()) + " votos");
            campoVotos.setForeground(blanco);
            
            nCandidatos.setText(String.valueOf(limite) + " candidatos");
            nCandidatos.setForeground(blanco);
            
            campoPartido.setText(partido1.getNombre());
            campoPartido.setHorizontalAlignment(0);
            campoPartido.setForeground(blanco);
            
            ImagenGanadores.setIcon(new ImageIcon(diputado.getImagen()));
            
            BufferedImage imagx =  modeloCandiatos.imagenPartidos(partido1);//le mando todos los campos incializados
           
            PartidoPolitico.setIcon(new ImageIcon(imagx));
        
            jLabel1.setText(partido1.getNombre());
            }else{
                    tituloLabel.setText("Diputados Ganadores");
                   jLabel1.setText("Nombre Partido");
                   Ganadores2.removeAllItems();
                   campoNombre.setText("Nombre Diputado");
                   campoPartido.setText("Partido Pol√≠tico");
                   campoVotos.setText("Votos Diputado");
                   nCandidatos.setText("N Candidatos");
                   ImagenGanadores.setIcon(null); 
            }
            
        }
         gif();
            
    }//GEN-LAST:event_Ganadores2ItemStateChanged

